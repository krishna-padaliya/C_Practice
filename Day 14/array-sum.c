#include <stdio.h>
void main()
{
	int a[5]={10,20,30,40,50};
	int b[5]={60,70,80,90,100};

	printf("The value of a[0] is=%d\n",a[0]);
	printf("The value of a[1] is=%d\n",a[1]);
	printf("The value of a[2] is=%d\n",a[2]);
	printf("The value of a[3] is=%d\n",a[3]);
	printf("The value of a[4] is=%d\n\n\n",a[4]);

	printf("The value of b[0] is=%d\n",b[0]);
	printf("The value of b[1] is=%d\n",b[1]);
	printf("The value of b[2] is=%d\n",b[2]);
	printf("The value of b[3] is=%d\n",b[3]);
	printf("The value of b[4] is=%d\n\n\n",b[4]);

	printf("The value of a[0] + b[0] is=%d\n",a[0]+b[0]);
	printf("The value of a[1] + b[1] is=%d\n",a[1]+b[1]);
	printf("The value of a[2] + b[2] is=%d\n",a[2]+b[2]);
	printf("The value of a[3] + b[3] is=%d\n",a[3]+b[3]);
	printf("The value of a[4] + b[4] is=%d\n",a[4]+b[4]);

}